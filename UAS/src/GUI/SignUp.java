    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        if (cek_input()){
            String username = txtUsername.getText();
            String password = new String(txtPassword.getPassword());
            if (!Main.cek_username(username)){
                JOptionPane.showMessageDialog(this, "Username Telh Digunakan, Silahkan Gunakan Username Lain!", "Warning!", JOptionPane.WARNING_MESSAGE);
            } else {
                Akun newAccount = new Akun(username, password);
                Main.dataAkun.add(newAccount);
                MyDatabase.insert_akun(username, password);
                JOptionPane.showMessageDialog(this, "Berhasil Mendaftarkan Akun", "Succes!", JOptionPane.INFORMATION_MESSAGE);
                txtPassword.setText("");
                txtPassword1.setText("");
            }
            txtUsername.setText("");
            txtUsername.requestFocus();
        }
    }//GEN-LAST:event_btnSignUpActionPerformed

