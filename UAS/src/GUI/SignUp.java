    private boolean cek_input(){
        String password = new String(txtPassword.getPassword());
        String password1 = new String(txtPassword1.getPassword());
        if (txtUsername.getText().isBlank()){
            JOptionPane.showMessageDialog(this, "Mohon Isi Username Terlebih Dahulu", "Warning!", JOptionPane.WARNING_MESSAGE);
            txtUsername.requestFocus();
            return false;
        } else if (password.isBlank()){
            JOptionPane.showMessageDialog(this, "Mohon Isi Password Terlebih Dahulu", "Warning!", JOptionPane.WARNING_MESSAGE);
            txtPassword.requestFocus();
            return false;
        } else if (password1.isBlank()){
            JOptionPane.showMessageDialog(this, "Mohon Isi Password Terlebih Dahulu", "Warning!", JOptionPane.WARNING_MESSAGE);
            txtPassword1.requestFocus();
            return false;
        } else if (!password.equalsIgnoreCase(password1)){
            JOptionPane.showMessageDialog(this, "Password dan Konfirmasi Password Harus Sama!", "Warning!", JOptionPane.WARNING_MESSAGE);
            txtPassword.requestFocus();
            return false;
        }
        return true;
    }
    
