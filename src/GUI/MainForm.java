    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tableManage.getSelectedRow();
        if (selectedRow != -1){
            int nim = Integer.parseInt(tableManage.getValueAt(selectedRow, 1).toString());
            int pilih = JOptionPane.showConfirmDialog(null, "Apakah yakin Ingin Menghapus Data Mahasiswa Dengan NIM "+ nim +"?", "Question", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (pilih == JOptionPane.YES_OPTION){
                MyDatabase.delete_mahasiswa(nim);
                MyDatabase.read_mahasiswa();
                txtSearchManage.setText("");
                display("");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih Data Mahasiswa Yang Ingin Dihapus Terlebih Dahulu!", "Warning!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

