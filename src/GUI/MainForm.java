    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (cek_input()){
            String nama = txtNama.getText();
            String jk;
            if (rbLaki.isSelected()){
                jk = rbLaki.getText();
            } else {
                jk = rbPerempuan.getText();
            }
            String fakultas = cbFakultas.getSelectedItem().toString();
            String prodi = cbProdi.getSelectedItem().toString();
            int nim = Integer.parseInt(txtNim.getText());
            double ipk = Double.parseDouble(IPK.getValue().toString());
            if (crud.equalsIgnoreCase("c")){
                if (Main.cek_nim(nim)){
                    MyDatabase.insert_mahasiswa(nama, jk, fakultas, prodi, nim, ipk);
                    Mahasiswa newMhs = new Mahasiswa(nama, nim, jk, fakultas, prodi, ipk);
                    Main.dataMahasiswa.add(newMhs);
                    NIM = nim;
                    JOptionPane.showMessageDialog(this, "Berhasil Menambah Data Mahasiswa!", "Success!", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "NIM Telah Terdaftar!", "Warning!", JOptionPane.WARNING_MESSAGE);
                    txtNim.requestFocus();
                }
            } else {
                if (Main.cek_nim(nim) || nim == NIM){
                    MyDatabase.update_mahasiswa(NIM, nim, nama, jk, fakultas, prodi, ipk);
                    MyDatabase.read_mahasiswa();
                    JOptionPane.showMessageDialog(this, "Berhasil Update Data Mahasiswa!", "Success!", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "NIM Telah Terdaftar!", "Warning!", JOptionPane.WARNING_MESSAGE);
                    txtNim.requestFocus();
                }
            }
            
        }
    }//GEN-LAST:event_btnSaveActionPerformed

