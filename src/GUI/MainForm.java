    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int selectedRow = tableManage.getSelectedRow();
        if (selectedRow != -1){
            NIM = Integer.parseInt(tableManage.getValueAt(selectedRow, 1).toString());
            txtNim.setText(tableManage.getValueAt(selectedRow, 1).toString());
            txtNama.setText(tableManage.getValueAt(selectedRow, 2).toString());
            if (tableManage.getValueAt(selectedRow, 3).toString().equalsIgnoreCase("Laki-laki")){
                rbLaki.setSelected(true);
                rbPerempuan.setSelected(false);
            } else {
                rbPerempuan.setSelected(true);
                rbLaki.setSelected(false);
            }
            cbFakultas.setSelectedItem(tableManage.getValueAt(selectedRow, 4).toString());
            cbProdi.setEnabled(true);
            generate_prodi(cbFakultas.getSelectedIndex());
            
            cbProdi.setSelectedItem(tableManage.getValueAt(selectedRow, 5).toString());
            IPK.setValue(Double.valueOf(tableManage.getValueAt(selectedRow, 6).toString()));
   
            crud = "u";
            lblManage.setText("Update Data Mahasiswa");
            ManageMain.setVisible(false);
            ManagePanel.setVisible(true);
            
        } else {
            JOptionPane.showMessageDialog(this, "Pilih Data Mahasiswa Yang Ingin Diupdate Terlebih Dahulu!", "Warning!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

