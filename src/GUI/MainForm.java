    private boolean cek_input(){
        if (txtNama.getText().isBlank()){
            JOptionPane.showMessageDialog(this, "Kolom Nama Harus Diisi!", "Warning!", JOptionPane.WARNING_MESSAGE);
            txtNama.requestFocus();
            return false;
        } else if (txtNim.getText().isBlank()){
            JOptionPane.showMessageDialog(this, "Kolom NIM Harus Diisi!", "Warning!", JOptionPane.WARNING_MESSAGE);
            txtNim.requestFocus();
            return false;
        } else if (!rbLaki.isSelected() && !rbPerempuan.isSelected()){
            JOptionPane.showMessageDialog(this, "Jenis Kelamin Harus Dipilih!", "Warning!", JOptionPane.WARNING_MESSAGE);
            return false;
        } else if (cbFakultas.getSelectedIndex() < 1){
            JOptionPane.showMessageDialog(this, "Kolom Fakultas Harus Dipilih!", "Warning!", JOptionPane.WARNING_MESSAGE);
            cbFakultas.requestFocus();
            return false;
        } else if (IPK.getValue().equals(0)){
            JOptionPane.showMessageDialog(this, "Kolom IPK Harus Diisi!", "Warning!", JOptionPane.WARNING_MESSAGE);
            IPK.requestFocus();
            return false;
        }
        int nim;
        double ipk;
        try {
            nim = Integer.parseInt(txtNim.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "NIM Harus Berupa Angka!", "Warning!", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        try {
            ipk = Double.parseDouble(IPK.getValue().toString());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "IPK Harus Berupa Angka Dari 0.1 sampai 4!", "Warning!", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        if (ipk <= 0 || ipk > 4){
            JOptionPane.showMessageDialog(this, "IPK Harus Berupa Angka Dari 0.1 sampai 4!", "Warning!", JOptionPane.WARNING_MESSAGE);
            IPK.requestFocus();
            return false;
        }
        return true;
    }
    
